-- เพิ่ม enum สำหรับประเภทงาน
create type assignment_category as enum ('practice', 'midterm', 'final', 'competency');

-- เพิ่ม 2 คอลัมน์ใน assignments
alter table assignments
  add column category assignment_category not null default 'practice',
  add column term       smallint           not null default 1
    check (term in (1, 2));

-- ลบ seed assignments เก่าออก
